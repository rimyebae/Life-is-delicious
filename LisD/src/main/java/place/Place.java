package place;

public class Place {
	
	private int placeID;
	private String placeName;
	private String placeAddress;
	private String placeFoodtype;
	private String placeRecommendation;
	private String placeNearstation;
	private String placeHtmlcode;
	private int placeAvailable;
	
	
	//getter setter
	public int getPlaceID() {
		return placeID;
	}
	public void setPlaceID(int placeID) {
		this.placeID = placeID;
	}
	public String getPlaceName() {
		return placeName;
	}
	public void setPlaceName(String placeName) {
		this.placeName = placeName;
	}
	public String getPlaceAddress() {
		return placeAddress;
	}
	public void setPlaceAddress(String placeAddress) {
		this.placeAddress = placeAddress;
	}
	public String getPlaceFoodtype() {
		return placeFoodtype;
	}
	public void setPlaceFoodtype(String placeFoodtype) {
		this.placeFoodtype = placeFoodtype;
	}
	
	public String getPlaceRecommendation() {
		return placeRecommendation;
	}
	public void setPlaceRecommendation(String placeRecommendation) {
		this.placeRecommendation = placeRecommendation;
	}
	
	public String getPlaceNearstation() {
		return placeNearstation;
	}
	public void setPlaceNearstation(String placeNearstation) {
		this.placeNearstation = placeNearstation;
	}

	public String getPlaceHtmlcode() {
		return placeHtmlcode;
	}
	public void setPlaceHtmlcode(String placeHtmlcode) {
		this.placeHtmlcode = placeHtmlcode;
	}
	
	public int getPlaceAvailable() {
		return placeAvailable;
	}
	public void setPlaceAvailable(int placeAvailable) {
		this.placeAvailable = placeAvailable;
	}
	

}
